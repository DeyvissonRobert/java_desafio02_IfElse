package br.com.alura.desafios.IfElse;


class ParOuImpar {
    public static void main(String[] args) {
        int valor = 33;
        if (valor % 2 == 0) {
            System.out.println("O número " + valor + " é par.");
        } else {
            System.out.println("O número " + valor + " é ímpar.");
        }
    }
}
